#include "../../minishell.h"

int	ft_pwd(void)
{
	char	pwd[1024];

	if (getcwd(pwd, 1024) != NULL)
	{
		printf("%s\n", pwd);
		return (1);
	}
	else
	{
		printf("pwd error\n");
		return (0);
	}
}
