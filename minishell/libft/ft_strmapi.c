#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	char	*result;
	size_t	i;

	len = ft_strlen(s);
	i = 0;
	result = (char *)malloc(sizeof(char) * len + 1);
	if (s == NULL || f == NULL)
		return (0);
	if (result == NULL)
		return (0);
	while (i < len)
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
