#include "../minishell.h"

bool	pwd(void)
{
	char	pwd[1024];

	if (getcwd(pwd, 1024) != NULL)
	{
		printf("pwd: %s\n", pwd);
		return (true);
	}
	else
	{
		printf("pwd error\n");
		return (false);
	}
}
